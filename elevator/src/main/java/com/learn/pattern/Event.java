package com.learn.pattern;

public class Event {
	private int moveFrom;
	private int moveTo;

	public int getMoveFrom() {
		return moveFrom;
	}

	public void setMoveFrom(int moveFrom) {
		this.moveFrom = moveFrom;
	}

	public int getMoveTo() {
		return moveTo;
	}

	public void setMoveTo(int moveTo) {
		this.moveTo = moveTo;
	}
}
